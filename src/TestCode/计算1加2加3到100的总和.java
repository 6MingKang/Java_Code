public class 计算1加2加3到100的总和 {
    public static void main(String[] args) {

        int kaishi = 1;
        int zongshu = 0;

        while (kaishi<=100){
            zongshu = zongshu + kaishi;
            kaishi++;

        }
        System.out.println(zongshu);
        System.out.println(zongshu);

    }
}
